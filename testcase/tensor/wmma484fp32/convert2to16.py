# 将每行的二进制字符串转换为16进制
binary_strings = [
"1011001 00101 10101 101 01010 1010111",
"1011001 00110 00110 101 01001 1010111",
"1011001 00110 10110 101 01010 1010111",
"1011001 00111 00111 101 01001 1010111",
"1011001 00111 10111 101 01010 1010111",
"1011001 01000 01000 101 01001 1010111",
"1011001 01000 11000 101 01010 1010111",
]

# 使用Python转换
hex_values = ["{:08X}".format(int(binary.replace(" ", ""), 2)) for binary in binary_strings]

for h in hex_values:
    print(h)
